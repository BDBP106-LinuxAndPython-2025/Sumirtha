# 'r'  : Read mode (default). Opens the file for reading. File must exist.
# 'w'  : Write mode. Creates a new file or overwrites an existing one.
# 'a'  : Append mode. Opens the file to append data at the end. Creates the file if it doesn't exist.
# 'x'  : Exclusive creation. Creates a new file. Raises an error if the file already exists.
# 'r+' : Read and write mode. File must exist.
# 'w+' : Write and read mode. Overwrites existing file or creates a new one.
# 'a+' : Append and read mode. Reads and appends. File is created if it doesn't exist.
# 'b'  : Binary mode. Used for reading or writing binary files (e.g., 'rb', 'wb').
# 't'  : Text mode (default). Used for reading or writing text files (e.g., 'rt', 'wt').
